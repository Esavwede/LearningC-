

namespace App.ArithmeticNamespace 
{
    class Arithmetic 
    {
        public double Add( double num1, double num2 )
        {
            return num1 + num2; 
        }

        public double Subtract( double num1, double num2 )
        {
            return num1 - num2; 
        }

        public double Mul(double num1, double num2 )
        {
            return num1 * num2; 
        }
    }
}